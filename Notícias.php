            <h2 id="título01">Projeto do IFRN une prevenção à Covid-19 e serviços de alimentação</h2>
            <p>Com a pandemia do novo coronavírus (Covid-19), os cuidados e hábitos de higienização vem merecendo atenção redobrada. Nessa perspectiva, o grupo de trabalho de Nutrição do IFRN, desenvolveu um projeto, a ser divulgado em formato de cartilha,  em relação às boas práticas para que não haja contaminação e propagação do vírus nos serviços de alimentação escolar.</p><br>
            <p>Boas Práticas são ações de higiene que devem ser obedecidas pelos manipuladores de alimentos desde o recebimento dos produtos a serem utilizados no preparo até a distribuição da alimentação para o consumidor. O fortalecimento dessas práticas reduz o risco de diversas enfermidades transmitidas pelos alimentos e adicionalmente contribui para diminuir a transmissão do COVID-19 pessoa a pessoa e por meio de superfícies e objetos contaminados no ambiente de produção, devido ao rigor com as práticas de higiene adotadas. </p>

            <h2 id="título02">Na cozinha do IFRN</h2>
            <p>O grupo de trabalho de Nutrição do IFRN é composto por nutricionistas de diferentes campi da instituição. Com o trabalho em casa e as diferentes demandas sanitárias da volta às aulas, esses profissionais decidiram construir um material que pudesse auxiliar os manipuladores de alimentos de escolas, como merendeiras,cozinheiros e auxiliares, a adotar boas práticas na produção de alimentos, pautados na prevenção da Covid-19.</p><br>
            <p>Pensando nesta nova realidade, a equipe elaborou o manual para capacitar os manipuladores de alimentos que atuam no Programa Nacional de Alimentação Escolar (Pnae), visando fortalecer tais boas práticas tanto durante o período de distribuição dos kits de alimentos aos pais ou responsáveis dos estudantes (Lei n° 13.987/2020) quanto no retorno das aulas.</p>

            <h2 id="título03">Qual o horário de funcionamento do cardápio IFRN?</h2>
            <p>O cardápio IFRN (site) fica disponível 24h por dia. Já o refeitório fica disponível de segunda à sexta.</p>

